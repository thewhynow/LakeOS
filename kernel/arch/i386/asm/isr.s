.section .text

.macro IMPL_ISR num
.global ISR\num
ISR\num:
    push $0 // push dummy error code
    push $\num
    jmp isr_common
.endm

.macro IMPL_ISR_ERROR_CODE num
.global ISR\num
ISR\num:
    push $\num // cpu pushes error code to stack for certain interrupts
    jmp isr_common
.endm

str:
.string "ISR_COMMON\n"

isr_common:
    pusha // pushes all general-purpose registers
    
    xorl %eax, %eax
    movw %ds, %ax
    pushl %eax

    movw $0x10, %ax
    movw %ax, %ds
    movw %ax, %es
    movw %ax, %fs
    movw %ax, %gs
    movw %ax, %ss

    pushl %esp
    call ISR_handler
    addl $4, %esp

    popl %eax // restore old segment
    movw %ax, %ds
    movw %ax, %es
    movw %ax, %fs
    movw %ax, %gs
    movw %ax, %ss

    popa
    add $8, %esp // remove error code and int_num from stack 
    iret

